class `increment, decrement, multification, divition`

fun main(args: Array<String>){

    var computerRam: Int = 17

    computerRam += 18

    println(computerRam)

    computerRam -= 1

    println(computerRam)

    computerRam -= 8

    println(computerRam)

    computerRam *= 2

    println(computerRam)

    computerRam /= 2

    println(computerRam)

    computerRam++ // 8 + 1

    println(computerRam)

    computerRam-- // 8 - 1

    println(computerRam)


}